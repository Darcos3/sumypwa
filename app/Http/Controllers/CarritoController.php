     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\Carrito  $carrito
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Carrito $carrito)
    {
        // dd($request->all());

        foreach ($request['producto'] as $id => $cantidad) {
            auth()->user()->carrito->productos()->syncWithoutDetaching([$id => ['cantidad' => $cantidad]]);
        }

        foreach ($request['combo'] as $id => $cantidad) {
            auth()->user()->carrito->combos()->syncWithoutDetaching([$id => ['cantidad' => $cantidad]]);
        }

        $carrito->actualizarTotalCarrito();

        return redirect('/carrito')->with('status', ['success', "La categoría ha sido creada con éxito"]);

    }

